package package6;

public class InfoAboutAccommodation {

	private int houseNumber;
	private int apartmentNumber;
	private boolean rent;
	private boolean voteForZe;
	
	
	int getHouseNumber() {
		return houseNumber;
	}
	void setHouseNumber(int houseNumber) {
		this.houseNumber = houseNumber;
	}
	int getApartmentNumber() {
		return apartmentNumber;
	}
	void setApartmentNumber(int apartmentNumber) {
		this.apartmentNumber = apartmentNumber;
	}
	boolean isRent() {
		return rent;
	}
	void setRent(boolean rent) {
		this.rent = rent;
	}
	boolean isVoteForZe() {
		return voteForZe;
	}
	void setVoteForZe(boolean voteForZe) {
		this.voteForZe = voteForZe;
	}
}
